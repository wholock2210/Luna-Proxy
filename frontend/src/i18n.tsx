type I18nContextValue = {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: string, values?: Record<string, string | number | boolean>) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

function normalizeLanguage(value: unknown): Language {
  return value === 'vi' ? 'vi' : 'en';
}

export function I18nProvider({children}: {children: React.ReactNode}) {
  const [language, setLanguageState] = useState<Language>(() => normalizeLanguage(window.localStorage.getItem('luna-ui-language')));

  useEffect(() => {
    let cancelled = false;
    fetch('/api/config')
      .then((res) => res.ok ? res.json() : null)
      .then((data) => {
        if (!cancelled && data?.settings?.ui?.language) {
          const backendLang = normalizeLanguage(data.settings.ui.language);
          setLanguageState(backendLang);
          window.localStorage.setItem('luna-ui-language', backendLang);
        }
      })
      .catch(() => {});
    return () => {
      cancelled = true;
    };
  }, []);

  const value = useMemo<I18nContextValue>(() => ({
    language,
    setLanguage: (nextLanguage) => {
      window.localStorage.setItem('luna-ui-language', nextLanguage);
      setLanguageState(nextLanguage);
      fetch('/api/config', {
        method: 'POST',
        headers: {'Content-Type': 'application/json'},
        body: JSON.stringify({
          settings: {
            ui: {
              language: nextLanguage
            }
          }
        })
      }).catch(err => console.error('Failed to sync language to backend:', err));
    },
    t: (key, values) => {
      const template = dictionaries[language][key] || dictionaries.en[key] || key;
      if (!values) return template;
      return Object.entries(values).reduce(
        (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
        template,
      );
    },
  }), [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error('useI18n must be used inside I18nProvider');
  return ctx;
}
